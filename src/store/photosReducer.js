export default function reducer (state = initialState, action ) {
  const { type, payload } = action;

  switch (type) {
    case 'PHOTO/INIT':
      return {...state, photos: payload, isLoading: false};
    
    case 'PHOTO/SEARCH':
      return {...state, photos: payload, isLoading: false};

    case 'PHOTO/TEST':
      return state;
    default:
      return state;
  }
}

